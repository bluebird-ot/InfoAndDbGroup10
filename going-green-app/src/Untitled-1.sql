CREATE TABLE "CEIMBUSCH"."USER_ACTIVITIES" 
   ( "ACTIVITY_ID" NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
     "USER_ID" NUMBER NOT NULL, 
     "ACTIVITY_TYPE" VARCHAR2(50 BYTE) NOT NULL, 
     "DISTANCE" NUMBER, 
     "TIME" NUMBER, 
     "CO2_EMISSION" NUMBER NOT NULL,
     "ACTIVITY_DATE" TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
     CONSTRAINT fk_user FOREIGN KEY ("USER_ID")
     REFERENCES "CEIMBUSCH"."USERS" ("USER_ID") 
   ) 
   TABLESPACE "USERS";
